let iconIdCounter = 0;

export function ProviderIcon({
  provider,
  size = 14,
  color,
  percentRemaining = 100,
}: ProviderIconProps & { provider: 'codex' | 'claude' | 'copilot' | 'cursor' | 'kimi' }) {
  const gradientId = useMemo(() => `provider-fade-${++iconIdCounter}`, []);
  const defaultColor = providerColors[provider];
  const fillColor = color || defaultColor;
  const usedPercent = 100 - Math.min(100, Math.max(0, percentRemaining));

  return (
    <Svg width={size} height={size} viewBox="0 0 24 24" fill="none">
      <Defs>
        <LinearGradient id={gradientId} x1="0" y1="0" x2="0" y2="1">
          <Stop offset={`${usedPercent}%`} stopColor={fillColor} stopOpacity={0.2} />
          <Stop offset={`${usedPercent}%`} stopColor={fillColor} stopOpacity={1} />
        </LinearGradient>
      </Defs>
      <Path d={ICON_PATHS[provider]} fill={`url(#${gradientId})`} />
    </Svg>
  );
}

